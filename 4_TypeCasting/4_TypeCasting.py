# 4) TYPE-CONVERSIONS AND CASTING :
# 4A) To Integers :
my_int = 5
print(my_int)
my_int = int(5.5)
print(my_int)
my_int = int("5")
print(my_int)
my_int = int(True)
print(my_int)

# 4B) To Floats :
my_float = 5.5
print(my_float)
my_float = float(5.5)
print(my_float)
my_float = float("5.6")
print(my_float)
my_float = float(True)
print(my_float)

# 4C) To Strings :
my_str = "Hello"
print(my_str)
my_str = str(3)
print(my_str)
my_str = str(5.6)
print(my_str)
my_str = str(True)
print(my_str)

# 4D) To Booleans :
my_bool = True
print(my_bool)
my_bool = bool(5)
print(my_bool)
my_bool = bool(33.3)
print(my_bool)
my_bool = bool("Hello, World")
print(my_bool)

